export function getTranslator(langName: string) {
  const code = (LANG_NAME_TO_CODE[langName] ?? 'en') as LangCode;
  const map = translations[code] ?? translations.en;
  return (key: string): string => map[key] ?? translations.en[key] ?? key;
}
